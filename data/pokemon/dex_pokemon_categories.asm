; PureRGBnote: CHANGED: Pokemon categories, e.g. BULBASAUR's is "SEED" were separated out and re-jigged to save space.
GetDexPokemonCategory:
	ld a, [wPokedexNum]
	dec a
	ld hl, PokemonCategoryMapping
	ld b, 0
	ld c, a
	add hl, bc
	ld a, [hl]
	ld hl, DexCategoryStrings
	and a
	jr z, .done
	ld c, a
.loopFindCategoryString
	ld a, [hli]
	cp '@'
	jr nz, .loopFindCategoryString
	dec c
	jr nz, .loopFindCategoryString
.done
	ld d, h
	ld e, l
	ret

